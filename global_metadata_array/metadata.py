def convert_bytes_to_int(bytes):
    return sum([int(b, 16) * (256 ** i) for i, b in enumerate(bytes)])

def read_json_data(path):
    json_file = open(path, encoding='utf-8')
    json_data = json.load(json_file)
    json_file.close()
    return json_data

def read_metadata(path):
    metadata_file = open(path, 'rb')
    metadata = metadata_file.read()
    metadata_file.close()
    return metadata

def update_arrays(json_data, metadata):
    for array in json_data:
        if array['value'] == '':
            offset = int(array['offset'], 16)
            size = array['size']
            slice = metadata[offset:offset+size]
            array['value'] = ' '.join('{:02X}'.format(x) for x in slice)
        else:
            value = int(array['value'], 10)
            bytes = []
            while value > 255:
                bytes.append(value % 256)
                value //= 256
            bytes.append(value)
            array['value'] = ' '.join('{:02X}'.format(x) for x in bytes)

def add_adjusted_array(json_data, item_size, ids_to_edit):
    for array in json_data:
        if array['id'] in ids_to_edit and 'adjusted_values' not in array:
            bytes = array['value'].split(' ')
            byte_sets = [bytes[i:i+item_size] for i in range(0, len(bytes), item_size)]
            array['adjusted_values'] = [convert_bytes_to_int(x) for x in byte_sets]

def save_arrays(json_data, output_path):
    output_file = open(output_path, "w", encoding='utf-8')
    json.dump(json_data, output_file, ensure_ascii=False, indent=4)
    output_file.close()


